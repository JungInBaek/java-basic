package variable;

public class PrimitiveDataType {

	public static void main(String[] args) {
		//	정수, 실수, 문자, 논리
		int age = 100;
		double height = 122.2;
		char gender = '남';
		boolean food = false;
		
		System.out.println("나의 나이는 " + age);
		System.out.println("나의 키는 " + height);
		System.out.println("나의 성별은 " + gender);
		System.out.println("내가 점심을 먹었는지 여부 " + food);
	}

}
